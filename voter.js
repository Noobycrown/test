const contractAddress = '0xA294E16973D127cFb2B93A301834413A05764fC2'; // Replace with your deployed contract address
let candidates = [];
let selectedCandidateId = null;

window.onload = async function() {
    if (typeof window.ethereum !== 'undefined') {
        web3 = new Web3(window.ethereum);
        await window.ethereum.enable(); // Request account access if needed
        contract = new web3.eth.Contract(contractABI, contractAddress);
        await loadCandidates();
    } else {
        alert('Please install MetaMask to use this dApp!');
    }
};

// Load candidates from the smart contract
async function loadCandidates() {
    const candidateList = await contract.methods.getCandidates().call();
    candidates = candidateList;

    const candidateDiv = document.getElementById('candidateList');
    candidateDiv.innerHTML = ''; // Clear any existing candidates

    candidates.forEach(candidate => {
        const candidateElement = document.createElement('div');
        candidateElement.innerHTML = `
            <input type="radio" name="candidate" value="${candidate.id}" onchange="selectCandidate(${candidate.id})">
            ${candidate.name} (Votes: ${candidate.voteCount})
        `;
        candidateDiv.appendChild(candidateElement);
    });
}

// Handle candidate selection
function selectCandidate(id) {
    selectedCandidateId = id;
}

// Handle the voting process
document.getElementById('voteButton').onclick = async function() {
    if (selectedCandidateId) {
        const accounts = await web3.eth.getAccounts();
        await contract.methods.vote(selectedCandidateId).send({ from: accounts[0] });
        alert('Vote casted for candidate ID: ' + selectedCandidateId);
        location.reload(); // Reload to update the vote counts
    } else {
        alert('Please select a candidate to vote for!');
    }
};